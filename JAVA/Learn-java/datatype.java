public class datatype {
    public static void main(String[] args){
        //정수형
        byte a=110;
        short b=32000;
        int c=2140483645;
        long d= 912475074561566L; //long은 L, l 추가
        //실수형
        float e= (float) 3.14159265358979; //float는 캐스팅 해야됨
        double f= 156.156156156156156156;
        String g="tars";
        char h='s';
        System.out.println(a+b+c+d);
        System.out.println(e+f);
        System.out.println(h+g);
    }
}
